    private void btnRegistrarTratamentoActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        int newId = -1;
        try {
            java.util.Date inicio = DateFormat.getDateInstance().parse(txtDataInicio.getText()); 
            java.util.Date fim = DateFormat.getDateInstance().parse(txtDataFim.getText());
            newId = Controller.addTratamento(new Date(inicio.getTime()), new Date(fim.getTime()), animal);
        } catch (ParseException ex) {
            Logger.getLogger(TratamentoView.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(newId >= 0){
            Tratamento tratamento = Controller.getTratamentoById(newId);
            TratamentoTableModel tb = (TratamentoTableModel)table_tratamentos.getModel();
            tb.addItem(tratamento);
        }
    }                                                      

    private void btnMarcarConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMarcarConsultaActionPerformed
        // TODO add your handling code here:
        
        java.util.Date data = null;
        try {
            data = DateFormat.getDateInstance().parse(txtCon.getText());
        } catch (ParseException ex) {
            Logger.getLogger(TratamentoView.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        int row = this.table_tratamentos.getSelectedRow();
        Tratamento t = ((TratamentoTableModel)this.table_tratamentos.getModel()).getTratamentoByRow(row);
        
        VeterinarioView v = new VeterinarioView(this.table_consultas, t, new Date(data.getTime()), txtHist.getText());
        v.setVisible(true);
    }//GEN-LAST:event_btnMarcarConsultaActionPerformed

    private void btnVerExamesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerExamesActionPerformed
        // TODO add your handling code here:
        int row = table_consultas.getSelectedRow();
        
        if (row >= 0){
            Consulta c = ((ConsultaTableModel) table_consultas.getModel()).getConsultaByRow(row);
            this.table_exames.setModel(new ExameTableModel((ArrayList)Controller.getExamesOfConsulta(c.getId())));
        }
    }//GEN-LAST:event_btnVerExamesActionPerformed

    private void btnMarcarExameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMarcarExameActionPerformed
        // TODO add your handling code here:
        String desc = Des_Exame.getText();
        int row = table_consultas.getSelectedRow();
        int rowT = table_tratamentos.getSelectedRow();
        ConsultaTableModel m = (ConsultaTableModel) table_consultas.getModel();
        Consulta consulta = m.getConsultaByRow(row);
        consulta.setTratamento(((TratamentoTableModel) table_tratamentos.getModel()).getTratamentoByRow(rowT));
        
        if (!desc.isEmpty() && consulta != null){
            int newId = Controller.addExame(consulta, desc);
            
            if (newId >= 0){
                m.addItem(Controller.getExameById(newId));
            }
        }
    }//GEN-LAST:event_btnMarcarExameActionPerformed

